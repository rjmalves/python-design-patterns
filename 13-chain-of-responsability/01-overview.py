
# Às vezes temos algumas questões para responder
# que podem precisar consultar mais de uma entidade
# para se obter a resposta.

# Por exemplo, em caso de comportamento não ético em uma
# empresa, quem deve ser o culpado? Isso depende..
#   - Empregado
#   - Gerente
#   - Diretor

# Quando clicamos em um elemento gráfico em uma forma,
# um botão trata o clique, interrompendo todo o processamento.
# Os grupos de caixas de formas acima da forma em questão
# também devem interromper o processamento.
# A janela que exibe todas as formas deve interromper o
# processamento.

# No caso de um jogo de cartas em computador, as cartas podem
# ter atributos de ataque e defesa, mas podendo ser melhoradas
# pelo efeito de outras cartas.

# Em geral, Chain of Responsability está relacionada com a chance
# de processar um comando um query, opcionalmente com uma implementação
# de processamento default, mas sempre com habilidade de terminar toda
# a cadeia de processamento.
