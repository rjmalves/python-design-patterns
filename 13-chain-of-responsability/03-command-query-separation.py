
# Existe um padrão de desenvolvimento que é o
# Command Query Separation (CQS).

# Um comando:
#   - Pedir por uma ação ou alteração (por favor,
#   torne o seu valor de attack = 2)

# Uma query:
#   - Pedir por uma informação (por favor,
#   forneça o seu valor atual de attack)

# CQS:
#   - Ter maneiras diferentes de enviar comandos
#   e queries para os objetos, ao invés de, por exemplo
#   apenas acesso direto aos campos.
