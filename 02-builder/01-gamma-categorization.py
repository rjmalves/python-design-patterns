# Todos os padrões de design são divididos em três categorias
# Isso é chamado de Gamma Categorization (Erich Gamma - GoF)

# Creational Patterns
# - Lida com a criação (construção) dos objetos
# - Explícitos (construtor) vs. implícitos (dependency injection, reflection, etc.)
# - Wholesale (único comando) vs. piecewise (passo a passo - objetos complexos)
# Builder, Abstract Factory, Factory Method, Prototype, Singleton

# Structural Patterns
# - Preocupação com a estrutura do programa (membros das classes)
# - Muitos padrões são wrappers que remetem à interface da classe interior
# - Estressam a importância de um bom design de API
# Adapter, Bridge, Composite, Decorator, Façade, Flyweight, Proxy

# Behavioral Patterns
# - São todos diferentes, não tem padrão.
# - Tratam de todos os temas, mas são únicos.
# Chain of Responsability, Command, Interpreter, Iterator, Mediator,
# Memento, Observer, State, Strategy, Template Method, Visitor
