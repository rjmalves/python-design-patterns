
# Geralmente, tudo que é desenvolvido tende a balancear
# complexidade e apresentação / usabilidade.

# Numa casa, por exemplo, existem muitos subsistemas
#   - Elétrico
#   - Sanitário
#   - etc..
# A estrutura interna é complexa, mas o usuário final
# não está exposto a esses detalhes.

# Com software, o princípio é o mesmo.
# Muitos sistemas podem estar funcionando para prover
# flexibilidade, mas os usuários das APIs só
# "querem que funcione".
